package looping;

public class ForLoop {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++) {
			System.out.println(i+" Bangladesh");
		}
		
		System.out.println("\n1 to 10 numbers");
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		
		System.out.println("\nAll even numbers");
		for(int i=2;i<=10;i=i+2) {
			System.out.println(i);
		}
		
		System.out.println("\nReverse number");
		for(int i=10;i>=1;i--) {
			System.out.println(i);
	}
	}
}
