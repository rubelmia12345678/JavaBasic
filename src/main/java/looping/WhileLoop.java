package looping;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i=1;
		
		while(i<=10) {
			
			 System.out.println(i+" Bangladesh");
			 i++;
		}
		System.out.println("\nEven numbers");
		int u=2;
		
		while(u<=10) {
			
			 System.out.println(u);
			 u=u+2;
	}
		System.out.println("\nOdd numbers");
		int odd=1;
		
		while(odd<=10) {
			
			 System.out.println(odd);
			 odd=odd+2;	
	}
}
}
