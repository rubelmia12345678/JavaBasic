package looping;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i =1;
		do {
			System.out.println(i+" Bangladesh");
			i++;
		}while(i<=10);
		
		System.out.println("\nReverse numbers");
		
		int dcrement =10;
		do {
			System.out.println(dcrement);
			dcrement--;
		}while(dcrement>=1);

}

}
