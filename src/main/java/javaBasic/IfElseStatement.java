package javaBasic;

public class IfElseStatement {

	public static void main(String[] args) {
		
		int age = 55;
		
		if(age<=45) {
			System.out.println("He is a young man");
		
		}
		
		else if(age<=60) {
			System.out.println("He is a senior citizen");
			
		}
			
		else {
			System.out.println("He is not a young man");
			
			
		}
		
	}
}




