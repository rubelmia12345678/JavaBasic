package extra;

public class GetEncapsulation {
	private String a;
	private int b;
	private String c;
	
	
	public void setA(String a) {
		this.a = a;
	}
	public String getA() {
		return a;
	}
	
	public void setB(int b) {
		this.b = b;
	}
	public int getB() {
		return b;
	}
	
	public void setC(String c) {
		this.c = c;
	}
	public String getC() {
		return c;
	}
	
	
	
}
